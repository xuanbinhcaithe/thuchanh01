public class ThucHanh02 {
    public static void main(String[] args) {
        int i = 10;
        float f = 20.5f;
        double d = 25.36;
        char c = 'b';
        boolean b = true;
        String str = "Hà Nội";

        System.out.println("i = "+i);
        System.out.println("f = "+f);
        System.out.println("d = "+d);
        System.out.println("c = "+c);
        System.out.println("b = "+b);
        System.out.println("str = "+str);
    }
}
